function add(n1,n2) {
    return n1 + n2;
  }
function subtract(n1,n2) {
    return n1 - n2;
}
function divide(n1,n2) {
    return n1 / n2 ; 
}
function multiply(n1,n2) {
    return n1 * n2 ; 
}
function increment(n){
    n++;
    return n; 
}
function decrement(n){
    n--;
    return n;
}
function makeInt(n) {
    n = parseInt(n, 10);
    return n;
}
function preserveDecimal(n) {
    return parseFloat(n);
}